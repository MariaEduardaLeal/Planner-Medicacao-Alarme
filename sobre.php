    <h1>Sobre o aplicativo</h1>
    <p>O projeto propõe a criação de um aplicativo que visa facilitar o registro, 
        planejamento e lembrança das medicações que um usuário precisa tomar em seu cotidiano.
        Com a utilização de um banco de dados pré-existente de medicamentos, 
        o usuário terá a opção de escolher entre diversas opções como cadastrar novas medicações.
        Além disso, ao escolher uma medicação, o usuário poderá baixar a bula do medicamento e 
        inserir informações personalizadas, aumentando a segurança e o conhecimento sobre os 
        medicamentos que está tomando. O aplicativo também oferecerá um sistema de alarmes/lembretes 
        e uma visualização do cronograma de medicações do usuário. Por fim, a geração de um histórico 
        medicamentoso completo possibilita um acompanhamento mais preciso por parte dos profissionais 
        de saúde, contribuindo para uma melhor qualidade de vida dos pacientes.</p>
